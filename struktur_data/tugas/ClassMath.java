public class ClassMath {

    public static void main(String[] args) {
    double i = 8.64;
    double j = 9.81;

    // Method log
    System.out.println("<----Method log---->");
    System.out.println("Hasil log10 "+i+" = "+Math.log(i));
    System.out.println("Hasil log10 "+j+" = "+Math.log(j));
    System.out.println();

    // Method log10
    System.out.println("<----Method log10---->");
    System.out.println("Hasil log10 "+i+" = "+Math.log10(i));
    System.out.println("Hasil log10 "+j+" = "+Math.log10(j));
    System.out.println();

    }
}